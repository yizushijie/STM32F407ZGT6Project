//////函		数：
//////功		能：
//////输入参数:
//////输出参数:
//////说		明：
//////////////////////////////////////////////////////////////////////////////
UINT8_T USARTTask_FuncTask(USART_HandlerType*USARTx, UINT8_T(*pFuncTask)(UINT8_T *, UINT8_T *))
{
	UINT16_T length = 0;
	if (USARTx != NULL)
	{
		//---判断接收是否完成
		if (USARTTask_GetReadState(USARTx) == 1)
		{
			//---CRC的校验
			if ((USARTTask_CRCTask_Read(USARTx) == OK_0) && (USARTTask_DeviceID(USARTx) == OK_0))
			{
				//---数据发送报头
				USARTTask_RealTime_AddByte(USARTx, USARTx->msgTxID);

				//---数据接收完成
				if (pFuncTask != NULL)
				{
					pFuncTask(USARTx->msgRxdHandler.pMsgVal, USARTx->msgTxdHandler.pMsgVal);
				}
				else
				{
					//USARTTask_FillMode_AddData(USARTx, USARTx->msgRxHandler.pMsgVal, length);
					//USARTTask_RealTime_AddByteSize(USARTx, USARTx->msgRxHandler.msgCount);
					USARTTask_RealTime_AddSize(USARTx, USARTx->msgRxdHandler.msgIndexW);
					for (length = USART1_ID_INDEX; length < USARTx->msgRxdHandler.msgCount; length++)
					{
						USARTTask_RealTime_AddByte(USARTx, USARTx->msgRxdHandler.pMsgVal[length]);
					}

					//---是否需要增加换行符
					if (USARTx->msgTxdHandler.msgAddNewLine==1)
					{
						USARTTask_RealTime_AddByte(USARTx, 0x0D);
						USARTTask_RealTime_AddByte(USARTx, 0x0A);
					}
					
				}

				//---判断是否发送CRC
				USARTTask_RealTime_AddCRC(USARTx);
			}
			else
			{
				//---发生CRC校验错误
				USART_Printf(USARTx, (void*)"=>>串口%d:发生CRC校验错误<<=\r\n", (USARTx->msgIndex - 1));
			}
			return USARTTask_Read_Init(USARTx);
		}
		return USARTTask_TimeOVFTask(USARTx);
	}
	return ERROR_2;
}

///////////////////////////////////////////////////////////////////////////////
//////函		数：
//////功		能：
//////输入参数:
//////输出参数:
//////说		明：
//////////////////////////////////////////////////////////////////////////////
UINT8_T USARTTask_DebugPollFuncTask(USART_HandlerType*USARTx, UINT8_T(*pFuncTask)(UINT8_T *, UINT8_T *))
{
	//UINT16_T length = 0;
	UINT32_T freqVal = 0;
	if (USARTx != NULL)
	{
		//---判断接收是否完成
		if (USARTTask_GetReadState(USARTx) == 1)
		{
			//---CRC的校验
			if ((USARTTask_CRCTask_Read(USARTx) == OK_0) && (USARTTask_DeviceID(USARTx) == OK_0))
			{
				if (USARTx->msgRxdHandler.pMsgVal[USART1_CMD_INDEX]==0xA4)
				{
					//---获取时钟频率
					TimerTask_CalcFreq_Task(0);

					freqVal = (UINT32_T)(pCalcFreq->msgFreqKHz[pCalcFreq->msgChannel] * 100);
				}

				USARTTask_FillMode_Init(USARTx,0);
				//USARTTask_FillMode_AddByte(USARTx, 0xA4);
				USARTTask_FillMode_AddByte(USARTx, 0x00);
				USARTTask_FillMode_AddByte(USARTx, (UINT8_T)(freqVal >> 24));
				USARTTask_FillMode_AddByte(USARTx, (UINT8_T)(freqVal >> 16));
				USARTTask_FillMode_AddByte(USARTx, (UINT8_T)(freqVal >> 8));
				USARTTask_FillMode_AddByte(USARTx, (UINT8_T)(freqVal ));

				USARTTask_FillMode_WriteSTART(USARTx, 0);
			}
			else
			{
				//---发生CRC校验错误
				USART_Printf(USARTx, "=>>串口%d:发生CRC校验错误<<=\r\n", (USARTx->msgIndex - 1));
			}
			return USARTTask_Read_Init(USARTx);
		}
		return USARTTask_TimeOVFTask(USARTx);
	}
	return ERROR_2;
}
