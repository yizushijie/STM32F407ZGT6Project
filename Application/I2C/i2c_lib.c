///////////////////////////////////////////////////////////////////////////////
//////函		数：
//////功		能：
//////输入参数:
//////输出参数:
//////说		明： 返回的是应答信号
//////////////////////////////////////////////////////////////////////////////
UINT8_T I2CLib_MSW_START(I2C_HandlerType *I2Cx, UINT8_T isWrite)
{
	//---发送启动信号
	I2C_MSW_START(I2Cx);

	//---判断是读取操作还是写入操作
	if (isWrite)
	{
		I2Cx->msgAddr &= 0xFE;
	}
	else
	{
		I2Cx->msgAddr |= 0x01;
	}

	//---发送从机地址
	I2C_MSW_SendByte(I2Cx, (UINT8_T)I2Cx->msgAddr);

	//---读取应答信号
	return I2C_MSW_ReadACK(I2Cx);
}

