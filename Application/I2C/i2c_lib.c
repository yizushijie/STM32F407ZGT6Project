///////////////////////////////////////////////////////////////////////////////
//////函		数：
//////功		能：
//////输入参数:
//////输出参数:
//////说		明：
//////////////////////////////////////////////////////////////////////////////
UINT8_T I2CLib_MSW_Send(I2C_HandlerType *I2Cx, UINT8_T *pVal, UINT16_T length)
{
	UINT8_T _return = OK_0;
	UINT16_T i = 0;

	//---发送启动信号并发送从机地址---写地址
	_return = I2CLib_MSW_START(I2Cx, 1);

	//---判断启动信号是否成功并同时决定是否发送数据
	if (_return == 0)
	{
		for (i = 0; i < length; i++)
		{
			//---发送数据
			I2CLib_MSW_SendByte(I2Cx, pVal[i]);

			//---读取应答信号
			_return = I2CLib_MSW_ReadACK(I2Cx);

			//---判断应答信号
			if (_return)
			{
				_return = (2 + i);
				break;
			}
		}
	}
	return _return;
}

///////////////////////////////////////////////////////////////////////////////
//////函		数：
//////功		能：
//////输入参数:
//////输出参数:
//////说		明：
//////////////////////////////////////////////////////////////////////////////
UINT8_T I2CLib_MSW_Read(I2C_HandlerType *I2Cx, UINT8_T *pVal, UINT16_T length)
{
	UINT8_T _return = OK_0;
	UINT16_T i = 0;

	//---发送启动信号并发送从机地址---写地址
	_return = I2CLib_MSW_START(I2Cx, 1);

	//---判断启动信号是否成功并同时决定是否发送数据
	if (_return != OK_0)
	{
		goto GotoExit;
	}

	//---发送启动信号并发送从机地址---读取地址
	_return = I2CLib_MSW_START(I2Cx, 0);

	//---判断启动信号是否成功并同时决定是否发送数据
	if (_return != OK_0)
	{
		_return = ERROR_2;
		goto GotoExit;
	}
	for (i = 0; i < length; i++)
	{
		//---读取数据
		pVal[i] = I2CLib_MSW_ReadByte(I2Cx);
		if ((length - 1) == i)
		{
			_return = ERROR_3;
		}

		//---发送应答信号或则不应答信号
		I2CLib_MSW_SendACK(I2Cx, _return);
	}
	_return = OK_0;
GotoExit:
	return _return;
}